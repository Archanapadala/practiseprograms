    internal class program4
    {
        static void Main(string[] args) 
        {
            //implicit type casting
            short x = 10;
            int y = x; 
            double z = y;
            Console.WriteLine("Value of x is " + x);
            Console.WriteLine("Value of y is "+y);
            Console.WriteLine("Value of z is "+z);

        }
    }
}
